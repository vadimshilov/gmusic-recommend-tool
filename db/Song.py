class Song:

    def __init__(self, id, google_id, name, album_id, rate, playcount, album_google_id, artist_id, ord):
        self.id = id
        self.google_id = google_id
        self.name = name
        self.album_id = album_id
        self.rate = rate
        self.playcount = playcount
        self.album_google_id = album_google_id
        self.artist_id = artist_id
        self.ord = ord
